def find_combinations(n):
    if n % 1000 != 0:
        print("금액은 1000원 단위여야 합니다.")
        return

    # //는 정수 나눗셈, 나눗셈 시 소수점은 제외 정수만
    # 50000원권, 10000원권, 5000원권, 1000원권의 반복문을 중첩                                                  11000원 일때
    for a in range(n // 50000 + 1):  # 50000원권 개수                                                           0
        for b in range((n - 50000 * a) // 10000 + 1):  # 10000원권 개수                                         2
            for c in range((n - 50000 * a - 10000 * b) // 5000 + 1):  # 5000원권 개수                           3
                for d in range((n - 50000 * a - 10000 * b - 5000 * c) // 1000 + 1):  # 1000원권 개수           12
                    # 모든 경우의 조합 출력
                    if 50000 * a + 10000 * b + 5000 * c + 1000 * d == n:
                        print(f"50000원권: {a}장, 10000원권: {b}장, 5000원권: {c}장, 1000원권: {d}장")

# 금액 입력
amount = int(input("금액을 입력하세요 (1000원 단위): "))
find_combinations(amount)
